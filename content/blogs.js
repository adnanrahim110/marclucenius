function normalizeSpaces(value) {
  return value.replace(/\s+/g, " ").trim();
}

function isPageMarker(line) {
  return /^--\s*\d+\s+of\s+\d+\s*--$/.test(line.trim());
}

function isBlogMarker(line) {
  return /^Blog\s+\d+\s*$/.test(line.trim());
}

function looksLikeTitleCaseHeading(line) {
  const words = line
    .replace(/[“”"():]/g, "")
    .split(/\s+/)
    .filter(Boolean);

  if (words.length < 3) return false;
  const uppercaseStarts = words.filter((word) => /^[A-Z]/.test(word)).length;
  return uppercaseStarts / words.length >= 0.6;
}

function isHeadingLine(line) {
  const trimmed = line.trim();
  if (!trimmed) return false;
  if (trimmed.length > 92) return false;
  if (trimmed.includes(".")) return false;
  if (/^\d+\)\s+/.test(trimmed)) return false;
  if (isBlogMarker(trimmed) || isPageMarker(trimmed)) return false;
  return looksLikeTitleCaseHeading(trimmed);
}

function isBlockquoteLine(line) {
  const trimmed = line.trim();
  return /^[“"].+[”"]$/.test(trimmed) && trimmed.length <= 180;
}

function isOrderedListStart(line) {
  return /^\d+\)\s+/.test(line.trim());
}

function shouldEndParagraph(line, nextLine) {
  const trimmed = line.trim();
  if (!nextLine) return true;
  if (isHeadingLine(nextLine) || isOrderedListStart(nextLine) || isBlockquoteLine(nextLine)) {
    return true;
  }
  return /[.!?][”"]?$/.test(trimmed) && trimmed.length < 95;
}

function parseOrderedList(lines, startIndex) {
  const items = [];
  let index = startIndex;

  while (index < lines.length) {
    const line = lines[index].trim();
    if (!line) {
      index += 1;
      continue;
    }
    if (isHeadingLine(line) || isBlockquoteLine(line)) break;

    if (isOrderedListStart(line)) {
      items.push(line.replace(/^\d+\)\s+/, "").trim());
      index += 1;
      continue;
    }

    if (items.length === 0) break;
    items[items.length - 1] = normalizeSpaces(`${items[items.length - 1]} ${line}`);
    index += 1;
  }

  return { items, nextIndex: index };
}

function parseBlocks(rawLines) {
  const blocks = [];
  let paragraph = "";

  const flushParagraph = () => {
    const text = normalizeSpaces(paragraph);
    if (text) blocks.push({ type: "p", text });
    paragraph = "";
  };

  for (let index = 0; index < rawLines.length; index += 1) {
    const line = rawLines[index].trim();
    if (!line) continue;
    if (isPageMarker(line) || isBlogMarker(line)) continue;

    const nextLine = rawLines[index + 1]?.trim() ?? "";

    if (isHeadingLine(line)) {
      flushParagraph();
      blocks.push({ type: "h2", text: normalizeSpaces(line) });
      continue;
    }

    if (isBlockquoteLine(line)) {
      flushParagraph();
      blocks.push({ type: "blockquote", text: normalizeSpaces(line) });
      continue;
    }

    if (isOrderedListStart(line)) {
      flushParagraph();
      const { items, nextIndex } = parseOrderedList(rawLines, index);
      blocks.push({ type: "ol", items: items.map(normalizeSpaces) });
      index = nextIndex - 1;
      continue;
    }

    paragraph = paragraph ? `${paragraph} ${line}` : line;
    if (shouldEndParagraph(line, nextLine)) {
      flushParagraph();
    }
  }

  flushParagraph();
  return blocks;
}

function postProcessBlocks(blocks) {
  const result = [];
  let index = 0;

  const groupUl = (predicate, minItems = 3) => {
    const items = [];
    let cursor = index;
    while (cursor < blocks.length) {
      const block = blocks[cursor];
      if (block.type !== "p" || !predicate(block.text)) break;
      items.push(block.text);
      cursor += 1;
    }
    if (items.length < minItems) return null;
    return { items, nextIndex: cursor };
  };

  const groupings = [
    { predicate: (text) => text.startsWith("A strategy"), min: 3 },
    { predicate: (text) => text.startsWith("It resists"), min: 2 },
    { predicate: (text) => text.startsWith("It might"), min: 3 },
    { predicate: (text) => /\bneeds\b/i.test(text) && text.length < 90, min: 3 },
    { predicate: (text) => text.endsWith("?"), min: 2 },
  ];

  while (index < blocks.length) {
    const block = blocks[index];

    if (block.type === "p") {
      let grouped = null;
      for (const grouping of groupings) {
        if (!grouping.predicate(block.text)) continue;
        grouped = groupUl(grouping.predicate, grouping.min);
        if (grouped) break;
      }

      if (grouped) {
        result.push({ type: "ul", items: grouped.items });
        index = grouped.nextIndex;
        continue;
      }
    }

    result.push(block);
    index += 1;
  }

  return result;
}

function countWords(blocks) {
  const text = blocks
    .map((block) => {
      if (block.type === "p" || block.type === "h2" || block.type === "blockquote") return block.text;
      if (block.type === "ol" || block.type === "ul") return block.items.join(" ");
      return "";
    })
    .join(" ");

  const words = normalizeSpaces(text).split(" ").filter(Boolean);
  return words.length;
}

function readingTimeLabel(wordCount) {
  const minutes = Math.max(1, Math.round(wordCount / 220));
  return `${minutes} min read`;
}

function parseRawBlogs(rawText) {
  const normalized = rawText.replace(/\r\n/g, "\n").replace(/Be0neath/g, "Beneath");
  const lines = normalized.split("\n").map((line) => line.trimEnd());

  const starts = [];
  for (let index = 0; index < lines.length; index += 1) {
    const match = lines[index].trim().match(/^Blog\s+(\d+)\s*$/);
    if (match) starts.push({ index, id: match[1] });
  }

  return starts.map((start, idx) => {
    const endIndex = starts[idx + 1]?.index ?? lines.length;
    const chunk = lines.slice(start.index + 1, endIndex);
    const cleaned = chunk
      .map((line) => line.trim())
      .filter((line) => line && !isPageMarker(line));

    const titleLines = [];
    let cursor = 0;
    while (cursor < cleaned.length && !cleaned[cursor].includes(".")) {
      titleLines.push(cleaned[cursor]);
      cursor += 1;
    }

    const title = normalizeSpaces(titleLines.join(" "));
    const blocks = postProcessBlocks(parseBlocks(cleaned.slice(cursor)));
    return { id: start.id, title, blocks };
  });
}

